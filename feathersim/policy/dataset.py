"""Behavior-cloning dataset: (observation → expert action) pairs from the hand-coded controller. [v2 Phase D]

The expert is the pure :func:`velocity_command` P-controller. Because its output depends only on the
goal in the body frame, a sample is generated by drawing a random body-frame goal and recording the twist
the expert would command — covering the full range of errors the policy will see while driving (no rollout
needed since the control law is memoryless).
"""

from __future__ import annotations

import math

import numpy as np

from feathersim.control.go_to_pose import PoseGains, velocity_command


def generate_dataset(
    n_samples: int = 20000, *, seed: int = 0, gains: PoseGains = PoseGains(),
    reach: float = 3.0,
) -> tuple[np.ndarray, np.ndarray]:
    """Return ``(observations (N,3), actions (N,3))`` — body-frame goals and the expert's twists.

    ``reach`` bounds the sampled forward/left error (metres); heading error spans ±pi.
    """
    rng = np.random.default_rng(seed)
    forward = rng.uniform(-reach, reach, n_samples)
    left = rng.uniform(-reach, reach, n_samples)
    dyaw = rng.uniform(-math.pi, math.pi, n_samples)
    obs = np.stack([forward, left, dyaw], axis=1).astype(np.float32)
    # Expert action: with the robot at the origin, a target of (forward, left, dyaw) *is* the body-frame
    # observation, so velocity_command returns exactly the twist for that observation.
    actions = np.array(
        [velocity_command((0.0, 0.0, 0.0), (f, l, d), gains) for f, l, d in obs], dtype=np.float32
    )
    return obs, actions
