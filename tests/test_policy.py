"""v2 Phase D: behavior-cloned go-to-pose policy.

The dataset, model, and the BC training/closed-loop comparison are all CPU-fast and sim-only (no GL),
so everything here runs headless. One GL-guarded test drives the full autonomy loop on the learned policy.
"""

import json
